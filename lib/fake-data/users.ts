// Función para obtener un usuario aleatorio
export function getRandomFakeUser(): FakeUser {
  const randomIndex = Math.floor(Math.random() * FAKE_USERS.length)
  return FAKE_USERS[randomIndex]
}

// Función para obtener un usuario aleatorio por género
export function getRandomFakeUserByGender(gender: "male" | "female"): FakeUser {
  const filteredUsers = FAKE_USERS.filter((user) => user.gender === gender)
  const randomIndex = Math.floor(Math.random() * filteredUsers.length)
  return filteredUsers[randomIndex]
}

// Función para obtener un usuario aleatorio que no esté en la lista de exclusión
export function getRandomFakeUserExcluding(excludeIds: string[]): FakeUser {
  const availableUsers = FAKE_USERS.filter((user) => !excludeIds.includes(user.id))

  if (availableUsers.length === 0) {
    // Si todos los usuarios están excluidos, devolver uno aleatorio de todos modos
    return getRandomFakeUser()
  }

  const randomIndex = Math.floor(Math.random() * availableUsers.length)
  return availableUsers[randomIndex]
}
